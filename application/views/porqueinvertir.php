<br>
<h4 class="font-italic">Ethereum</h4>
<br><br><br>
<p class="font-weight-light">
Ethereum es la segunda criptomoneda más grande en términos de capitalización de mercado , solo por detrás de bitcoin, y es la tercera por volumen comercializado diario, por detrás de Tether y bitcoin.

Ethereum fue creado por Vitalik Buterin en 2014 y es la plataforma más grande para aplicaciones descentralizadas también conocida como DApps. Ethereum es la plataforma y Ether es la moneda utilizada en la plataforma Ethereum.

Al igual que Bitcoin, la plataforma Ethereum está descentralizada y permite a las personas conectarse directamente a través de una supercomputadora potente sin la necesidad de una autoridad central.

¿Cómo funciona Ethereum? Ethereum funciona gracias a Smart Contracts, un conjunto de condiciones y acciones creadas por sus desarrolladores y ejecutadas por la red teniendo en cuenta un conjunto de reglas definidas.

¿Ethereum sigue siendo una buena inversión en 2021? ¡Sí lo es! Debido a las capacidades de sus contratos inteligentes, Ethereum es muy popular y valioso.<br><br></p>
<h4 class="font-italic">Bitcoin</h4><br><br><br>
<p class="font-weight-light">
Bitcoin es la primera y la criptomoneda más grande y popular del mundo en el mercado. Fue creado en 2009 por Satoshi Nakamoto, un desarrollador de software anónimo cuya identidad no se ha revelado hasta el día de hoy.

Tenía una visión de un método de pago independiente que podía transferirse sin usar bancos. Es difícil pensar en una cartera de cifrado sin bitcoin.

Hay varias razones por las cuales bitcoin siempre se ha considerado como una inversión razonable está descentralizado, es decir, no hay una institución central que controle el suministro de bitcoin. El suministro de bitcoin es limitado. Las monedas fiduciarias se pueden imprimir siempre que se necesiten y su suministro es ilimitado. Bitcoin tiene un suministro máximo de 21 millones de bitcoin y ya se han 18,715 millones, según datos de Blockchain.com.

Las transacciones de bitcoin son anónimas. Dado que bitcoin no se rige por una entidad central, los usuarios no tienen que identificarse para poder realizar transacciones.
No obstante, una vez confirmadas, las transacciones de bitcoin no se pueden revertir, cambiar o eliminar.</p><br><br>
<h4 class="font-italic">Litecoin</h4>
<br><br><br>
<p class="font-weight-light">
Litecoin es una bifurcación de Bitcoin y una red global descentralizada de pagos de código abierto.

Tiene un experimentado equipo de desarrollo trabajando en él, encabezado por Charlie Lee, que trabajó para Google y Coinbase.

Litecoin es una moneda muy innovadora y ha introducido varias mejoras e innovaciones en la tecnología blockchain que luego también fueron aceptadas e implementadas por Bitcoin. Esos incluyen SegWit y Atomic Swaps.

En términos de volumen diario, Litecoin es una de las criptomonedas más utilizadas debido a sus transacciones instantáneas de costo cercano a cero. Esto también hace que Litecoin sea una excelente opción para comprar otras criptomonedas. Tiene más de 400 pares comerciales disponibles en una larga lista de intercambios.</p>
